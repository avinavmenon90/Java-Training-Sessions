package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {


	// Primitive Data Types in Java
	//1. int:
		int i=10;
		int j=20;
		j=35;
		

	//2. double:
		double d = 12.33;
		double d1 = 34.44;
		double d2 = 100; //represented as 100.00
		
	//3. char:
		char c = 'a';
		char c1 = 'A';
		char c2 = '1'; //1 is treated as a character
		char c4 = 'aa'; //invalid - only single digit character
		
	//4. boolean:
		boolean b1 = true;
		boolean b2 = false;
		
		
//NOT a primitive data type		
	//5. String: it's a class, NOT a datatype. But it can be used like a datatype
		String s = "Hello @ world 123";
		String s2 = "1000"; //1000 treated as a String
	}

}
